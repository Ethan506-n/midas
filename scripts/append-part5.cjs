add("    else if (k === 'max-age') { const n = parseInt(v, 10); if (!isNaN(n)) cookie.expires = Date.now() + n * 1000; }");
add("    else if (k === 'secure') cookie.secure = true;");
add("    else if (k === 'httponly') cookie.httpOnly = true;");
add("  }");
add("  return cookie;");
