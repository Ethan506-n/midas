add("function parseSetCookie(str) {");
add("  const parts = str.split(';').map(s => s.trim()).filter(Boolean);");
add("  if (!parts.length) return null;");
add("  const nv = parts.shift();");
add("  const eq = nv.indexOf('=');");
add("  if (eq < 0) return null;");
add("  const cookie = {");
add("    name: nv.slice(0, eq).trim(),");
add("    value: nv.slice(eq + 1).trim(),");
add("    path: '/',");
add("    domain: null,");
add("    expires: null,");
add("    secure: false,");
add("    httpOnly: false,");
add("  };");
add("  for (const a of parts) {");
add("    const ei = a.indexOf('=');");
add("    const k = (ei < 0 ? a : a.slice(0, ei)).trim().toLowerCase();");
add("    const v = ei < 0 ? '' : a.slice(ei + 1).trim();");
add("    if (k === 'path') cookie.path = v || '/';");
add("    else if (k === 'domain') cookie.domain = v.replace(/^\\./, '').toLowerCase();");
